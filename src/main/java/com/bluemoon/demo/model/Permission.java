package com.bluemoon.demo.model;

public class Permission {
	private String pmsId;
	private String pmsCode;
	private String pmsName;
	private String pmsModule;

	public String getPmsId() {
		return pmsId;
	}

	public void setPmsId(String pmsId) {
		this.pmsId = pmsId;
	}

	public String getPmsCode() {
		return pmsCode;
	}

	public void setPmsCode(String pmsCode) {
		this.pmsCode = pmsCode;
	}

	public String getPmsName() {
		return pmsName;
	}

	public void setPmsName(String pmsName) {
		this.pmsName = pmsName;
	}

	public String getPmsModule() {
		return pmsModule;
	}

	public void setPmsModule(String pmsModule) {
		this.pmsModule = pmsModule;
	}

}
